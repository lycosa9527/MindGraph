function spToPt(s: string): string {
  let out = s
  const ordered = [...REPS].sort((a, b) => b[0].length - a[0].length)
  for (const [from, to] of ordered) {
    out = out.split(from).join(to)
  }
  return out
}

function main(): void {
  const flat = es as Record<string, string>
  const lines: string[] = [
    '/** pt UI — canvas (Brazilian/European neutral UI Portuguese) */',
    'export default {',
  ]
  for (const key of Object.keys(flat)) {
    lines.push(`  ${JSON.stringify(key)}: ${JSON.stringify(spToPt(flat[key]))},`)
  }
  lines.push('} as const', '')
  writeFileSync(OUT, lines.join('\n'), 'utf8')
}

