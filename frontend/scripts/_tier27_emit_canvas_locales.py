def _nl_key_multiline_rows(text: str) -> list[tuple[str, bool]]:
    """Return (key, multiline_value) in export order; multiline matches nl line breaks."""
    body = text.split("export default {", 1)[1].rsplit("}", 1)[0]
    lines = body.splitlines()
    rows: list[tuple[str, bool]] = []
    idx = 0
    while idx < len(lines):
        line = lines[idx].rstrip()
        match = re.match(r"^(\s*)'([^']+)':\s*(.*)$", line)
        if not match:
            idx += 1
            continue
        key, rest = match.group(2), match.group(3).strip()
        if rest == "":
            rows.append((key, True))
            idx += 2
        else:
            rows.append((key, False))
            idx += 1
    return rows


def _encode_ts_single_quoted(value: str) -> str:
    parts: list[str] = []
    for char in value:
        if char == "\\":
            parts.append("\\\\")
        elif char == "'":
            parts.append("\\'")
        else:
            parts.append(char)
    return "".join(parts)


def emit_locale(header_locale: str, values: list[str], out_path: Path) -> None:
    nl_text = NL_PATH.read_text(encoding="utf-8")
    rows = _nl_key_multiline_rows(nl_text)
    if len(rows) != len(values):
        raise SystemExit(
            f"{header_locale}: key count {len(rows)} != values {len(values)}"
        )
    out_lines = [
        "/**",
        f" * {header_locale} UI — canvas",
        " */",
        "",
        "export default {",
    ]
    for (key, multiline), val in zip(rows, values, strict=True):
        esc = _encode_ts_single_quoted(val)
        if multiline:
            out_lines.append(f"  '{key}':")
            out_lines.append(f"    '{esc}',")
        else:
            out_lines.append(f"  '{key}': '{esc}',")
    out_lines.append("}")
    out_lines.append("")
    out_path.write_text("\n".join(out_lines), encoding="utf-8")


